public class SquareRoot_PowerOf {
    public static void main(String[] args) {
        double a, b;
        a = Math.pow(25, 2); // Metoda Math.pow() primeste 2 argumente: baza(25) si exponentul (2)
        b = Math.sqrt(25);

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
