package ForLoop;

public class EvenNumber {
    public static void main(String[] args) {

        // Bucla for combinata cu instructiune if ce utilizezza operatorul modulo
        for (int i = 0; i <= 20; i++) {
            if (i % 2 == 0)
                System.out.println(i);
        }

        System.out.println("=====================");


    }
}
